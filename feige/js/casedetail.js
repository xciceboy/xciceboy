function Arg(key) {
    var arr = (location.hash || location.search || '').split('?')[1].split('&')
    var params = {}
    for (var i = 0; i < arr.length; i++) {
        var data = arr[i].split('=')
        if (data.length === 2) {
            params[data[0]] = data[1]
        }
    }
    return params[key]
}